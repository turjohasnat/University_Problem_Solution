//C Solution:
#include<stdio.h>
#include<stdbool.h>

int main(){
    double double_value;
    bool bool_value;

    double_value = 3.140000e+00;
    bool_value = 1;

    printf("The double value: %e\n", double_value);
    printf("The boolean value: %d\n\n", bool_value);


    double doubleValue;
    bool boolValue;

    doubleValue = 1.618039;
    boolValue = 0;

    printf("The double value: %lf\n", doubleValue);
    printf("The boolean value: %d\n", boolValue);

    return 0;
}