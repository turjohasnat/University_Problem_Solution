//C Solution:
#include<stdio.h>

int main(){
    int integer_num;
    scanf("%d", &integer_num);
    printf("The integer value: %d\n", integer_num);

    float float_num;
    scanf("%f", &float_num);
    printf("The floating point value: %f\n", float_num);

    char character;
    scanf(" %c", &character);
    printf("The character value: %c\n", character);

    return 0;
}