//Task - Program that will defineaconstant using “CONST” and print the value.

//Sample output:
//The value of pi: 3.14
//The value of golden ratio: 1.62

//C Solution:
#include<stdio.h>

int main(){
    const float pi=3.14;
    const float goldenRatio=1.62;

    printf("The value of pi:%.2f\n",pi);
    printf("The value of golden ratio:%.2f\n",goldenRatio);

    return 0;
}
